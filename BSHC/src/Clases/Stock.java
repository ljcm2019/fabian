package Clases;


public class Stock {
private int opera,canti;

    
    public Stock(int opera,int canti){
        this.opera=opera;
        this.canti=canti;
    } 
    public int getOperacion() {
        return opera;
    
    }

    public int getCantidad() {
        return canti;
    }

    

}
