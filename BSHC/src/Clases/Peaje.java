
package Clases;


public class Peaje {
    private int tipo;
    private int cantidad;
    public Peaje (int tipo,int cantidad){
        this.tipo=tipo;
        this.cantidad=cantidad;
    }
    public int getTipo() {
        return tipo;
    }

    public int getCantidad() {
        return cantidad;
    }
    
    
    
}
